int	ft_str_is_numeric(char *str)
{
	int	i;
	int	x;

	i = 0;
	x = 1;
	while (str[i] != '\0' && x == 1)
	{
		if (str[i] >= '0' && str[i] <= '9')
			x = 1;
		else
			x = 0;
	i++;
	}
	return (x);
}
